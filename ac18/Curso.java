package ProgramacaoOrientadaAObjetos.AcProgramacaoOrientadaAObjetos.ac18;

public class Curso {
    
    private float ap1;
    private float ap2;
    private float ac;

    public Curso(float ap1, float ap2, float ac) {
        this.ap1 = ap1;
        this.ap2 = ap2;
        this.ac = ac;
    }

    public float getAp1() {
        return ap1;
    }

    public void setAp1(float ap1) {
        this.ap1 = ap1;
    }

    public float getAp2() {
        return ap2;
    }

    public void setAp2(float ap2) {
        this.ap2 = ap2;
    }

    public float getAc() {
        return ac;
    }

    public void setAc(float ac) {
        this.ac = ac;
    }

}
